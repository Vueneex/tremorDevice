// ===== BIT-BANG SPI (STM32 HAL ile) =====
void MicroDelay(uint32_t delay) {
    // Loop-based mikro-delay (like Tremor)
    for(volatile uint32_t i = 0; i < delay * 2; i++);
}

uint8_t BitBang_IO(uint8_t data) {
    uint8_t received = 0;
    
    // MSB-first bit transmission (bit 7 to bit 0)
    for (int i = 7; i >= 0; i--) {
        // MOSI'ye bit yaz (Arduino digitalWrite)
        digitalWrite(BMI270_MOSI, (data & (1 << i)) ? HIGH : LOW);
        MicroDelay(10);  // 5µs → 10µs (increased timing)
        
        // SCK HIGH
        digitalWrite(BMI270_SCK, HIGH);
        MicroDelay(10);
        
        // MISO'dan bit oku (Arduino digitalRead)
        if (digitalRead(BMI270_MISO)) {
            received |= (1 << i);
        }
        
        // SCK LOW
        digitalWrite(BMI270_SCK, LOW);
        MicroDelay(10);
    }
    
    return received;
}

// ===== BMI270 REGISTER OPERATIONS (Arduino API + increased timing) =====
void BMI270_Write(uint8_t reg, uint8_t val) {
    // CS LOW
    digitalWrite(BMI270_CS, LOW);
    BitBang_IO(reg & 0x7F);
    BitBang_IO(val);
    // CS HIGH
    digitalWrite(BMI270_CS, HIGH);
}

uint8_t BMI270_Read(uint8_t reg) {
    uint8_t res = 0;
    // CS LOW
    digitalWrite(BMI270_CS, LOW);
    BitBang_IO(reg | 0x80);
    BitBang_IO(0x00);  // Dummy byte
    res = BitBang_IO(0x00);  // Read actual result
    // CS HIGH
    digitalWrite(BMI270_CS, HIGH);
    return res;
}

void BMI270_BurstWrite(uint8_t reg, const uint8_t *data, uint16_t len) {
    // CS LOW
    digitalWrite(BMI270_CS, LOW);
    BitBang_IO(reg & 0x7F);
    for(uint16_t i = 0; i < len; i++) {
        BitBang_IO(data[i]);
    }
    // CS HIGH
    digitalWrite(BMI270_CS, HIGH);
}

uint8_t BMI270_LoadConfig(void) {
    BMI270_Write(0x7C, 0x00);
    delay(1);
    BMI270_Write(0x59, 0x00);
    delay(1);
    
    uint16_t index = 0;
    while(index < BMI270_CONFIG_SIZE) {
        uint16_t word_index = index / 2;
        BMI270_Write(0x5B, (uint8_t)(word_index & 0x0F));
        BMI270_Write(0x5C, (uint8_t)((word_index >> 4) & 0xFF));
        
        uint16_t chunk = BMI270_CONFIG_SIZE - index;
        if(chunk > 256) chunk = 256;
        BMI270_BurstWrite(0x5E, &bmi270_config_data[index], chunk);
        index += chunk;
    }
    
    BMI270_Write(0x59, 0x01);
    delay(20);
    uint8_t status = BMI270_Read(0x21);
    return (status & 0x0F) == 0x01 ? 1 : 0;
}

void setup() {
    // Basic LED setup
    pinMode(LED_PIN, OUTPUT);
    digitalWrite(LED_PIN, HIGH);  // LED ON to show we're in setup
    
    Serial.begin(115200);
    delay(1000);
    
    Serial.println("=== SETUP STARTED ===");
    Serial.flush();
    
    // Simple test - just try to read chip ID
    pinMode(BMI270_CS, OUTPUT);
    pinMode(BMI270_SCK, OUTPUT);
    pinMode(BMI270_MOSI, OUTPUT);
    pinMode(BMI270_MISO, INPUT);
    
    digitalWrite(BMI270_CS, HIGH);
    digitalWrite(BMI270_SCK, LOW);
    digitalWrite(BMI270_MOSI, LOW);
    
    Serial.println("Pins configured");
    Serial.flush();
    
    delay(500);
    
    // Try to read chip ID
    uint8_t chip_id = BMI270_Read(0x00);
    Serial.print("Chip ID read: 0x");
    Serial.println(chip_id, HEX);
    Serial.flush();
    
    if(chip_id == 0x24) {
        sensorHazir = true;
        Serial.println("BMI270 DETECTED!");
    } else {
        Serial.println("BMI270 NOT DETECTED!");
    }
    
    digitalWrite(LED_PIN, LOW);  // LED OFF to show setup complete
    Serial.println("=== SETUP COMPLETE ===");
    Serial.flush();
}

void loop() {
    static uint8_t count = 0;
    static uint8_t once = 0;
    
    if(once == 0) {
        once = 1;
        Serial.write('S');
        Serial.write('E');
        Serial.write('T');
        Serial.write('U');
        Serial.write('P');
        Serial.write('\n');
        Serial.flush();
        delay(500);
    }
    
    if(count == 0) {
        Serial.println("### LOOP BAŞLADI - Sensor Status Check ###");
        Serial.println(sensorHazir ? "Sensor: HAZIR" : "Sensor: BULUNAMADI");
        Serial.flush();
    }
    count++;
    if(count >= 50) count = 0;  // Her 1 saniyede bir mesaj
    
    int rawBat = analogRead(BATTERY_PIN);
    float bataryaYuzdesi = map(rawBat, 0, 4095, 0, 100);
    
    if(sensorHazir) {
        uint8_t x_l = BMI270_Read(0x0C);
        uint8_t x_m = BMI270_Read(0x0D);
        uint8_t y_l = BMI270_Read(0x0E);
        uint8_t y_m = BMI270_Read(0x0F);
        uint8_t z_l = BMI270_Read(0x10);
        uint8_t z_m = BMI270_Read(0x11);
        uint8_t gx_l = BMI270_Read(0x12);
        uint8_t gx_m = BMI270_Read(0x13);
        uint8_t gy_l = BMI270_Read(0x14);
        uint8_t gy_m = BMI270_Read(0x15);
        uint8_t gz_l = BMI270_Read(0x16);
        uint8_t gz_m = BMI270_Read(0x17);
        
        int16_t acc_x = (int16_t)((x_m << 8) | x_l);
        int16_t acc_y = (int16_t)((y_m << 8) | y_l);
        int16_t acc_z = (int16_t)((z_m << 8) | z_l);
        int16_t gyro_x = (int16_t)((gx_m << 8) | gx_l);
        int16_t gyro_y = (int16_t)((gy_m << 8) | gy_l);
        int16_t gyro_z = (int16_t)((gz_m << 8) | gz_l);
        
        float acc_x_g = acc_x / 4096.0f;
        float acc_y_g = acc_y / 4096.0f;
        float acc_z_g = acc_z / 4096.0f;
        float gyro_x_dps = gyro_x / 16.384f;
        float gyro_y_dps = gyro_y / 16.384f;
        float gyro_z_dps = gyro_z / 16.384f;
        
        Serial.print(acc_x_g); Serial.print(",");
        Serial.print(acc_y_g); Serial.print(",");
        Serial.print(acc_z_g); Serial.print(",");
        Serial.print(gyro_x_dps); Serial.print(",");
        Serial.print(gyro_y_dps); Serial.print(",");
        Serial.print(gyro_z_dps); Serial.print(",");
        Serial.println(bataryaYuzdesi);
    } else {
        Serial.println("ERR,SENSOR_BULUNAMADI");
        delay(500);
    }
    
    delay(20);
}
